const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>("en");

  const t = (key: string, params?: Record<string, string | number>) => {
    let translation = translations[language][key] || key;
    
    // Replace parameters like {{count}} with actual values
    if (params) {
      Object.keys(params).forEach(param => {
        translation = translation.replace(`{{${param}}}`, String(params[param]));
      });
    }
    
    return translation;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    console.error("useLanguage called outside LanguageProvider. Check that Root.tsx wraps routes with LanguageProvider.");
    throw new Error("useLanguage must be used within a LanguageProvider. Please refresh the page (Cmd/Ctrl + Shift + R)");
  }
  return context;
}

